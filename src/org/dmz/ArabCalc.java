package org.dmz;

public class ArabCalc {

    private static int result;

    public static int doCalc(int num1, int num2, char operation){

        switch (operation) {
            case '+':
                result = num1 + num2;
                break;
            case '-':
                result = num1 - num2;
                break;
            case '*':
                result = num1 * num2;
                break;
            case '/':
                result = num1 / num2;
                break;
        }
        return result;
    }
}
